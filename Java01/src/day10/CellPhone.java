package day10;

public class CellPhone {
	String model;
	String number;
	int chord;
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public int getChord() {
		return chord;
	}
	public void setChord(int chord) {
		this.chord = chord;
	}

}
